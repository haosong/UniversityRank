package com.shawnsong.universityrank.entity;

public class UniversityRanking {
    private String overallScore;
    private String resourceScore;
    private String engagementScore;
    private String outcomesScore;
    private String environmentScore;
    private String tuitionFees;
    private String roomAndBoard;
    private String salaryTenYears;

    public UniversityRanking() {

    }

    public String getOverallScore() {
        return overallScore;
    }

    public void setOverallScore(String overallScore) {
        this.overallScore = overallScore;
    }

    public String getResourceScore() {
        return resourceScore;
    }

    public void setResourceScore(String resourceScore) {
        this.resourceScore = resourceScore;
    }

    public String getEngagementScore() {
        return engagementScore;
    }

    public void setEngagementScore(String engagementScore) {
        this.engagementScore = engagementScore;
    }

    public String getOutcomesScore() {
        return outcomesScore;
    }

    public void setOutcomesScore(String outcomesScore) {
        this.outcomesScore = outcomesScore;
    }

    public String getEnvironmentScore() {
        return environmentScore;
    }

    public void setEnvironmentScore(String environmentScore) {
        this.environmentScore = environmentScore;
    }

    public String getTuitionFees() {
        return tuitionFees;
    }

    public void setTuitionFees(String tuitionFees) {
        this.tuitionFees = tuitionFees;
    }

    public String getRoomAndBoard() {
        return roomAndBoard;
    }

    public void setRoomAndBoard(String roomAndBoard) {
        this.roomAndBoard = roomAndBoard;
    }

    public String getSalaryTenYears() {
        return salaryTenYears;
    }

    public void setSalaryTenYears(String salaryTenYears) {
        this.salaryTenYears = salaryTenYears;
    }
}
